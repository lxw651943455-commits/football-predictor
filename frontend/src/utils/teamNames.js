// 获取球队中文名称
export function getTeamName(englishName) {
  return TEAM_NAMES[englishName] || englishName;
}

// 批量转换
export function convertTeamNames(matches) {
  return matches.map(match => ({
    ...match,
    home_team_cn: getTeamName(match.home_team),
    away_team_cn: getTeamName(match.away_team),
  }));
}
